import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, financialData } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    // Build context from financial data
    const context = financialData ? `
Financial Context:
- Total Transactions: ${financialData.transactionCount || 0}
- Total Revenue: $${financialData.totalRevenue || 0}
- Total Expenses: $${financialData.totalExpenses || 0}
- Cash Balance: $${financialData.cashBalance || 0}
- Outstanding Invoices: ${financialData.outstandingInvoices || 0}
- Active Loans: ${financialData.activeLoans || 0}
` : '';

    const systemPrompt = `You are a Virtual CFO (VCFO) AI assistant specialized in financial analysis and business insights for SMEs. 

${context}

Your role is to:
1. Analyze financial data and provide actionable insights
2. Answer questions about cash flow, expenses, revenue trends
3. Identify potential financial risks and opportunities
4. Recommend strategies for improving financial health
5. Explain financial metrics in simple, business-friendly language

Always be:
- Professional yet approachable
- Data-driven in your analysis
- Proactive in identifying issues
- Clear and concise in explanations
- Focused on actionable recommendations`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Payment required. Please add credits to your Lovable AI workspace." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(
        JSON.stringify({ error: "AI gateway error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("VCFO chat error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});